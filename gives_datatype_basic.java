package basic_cheeze_JAVA;

public class gives_datatype_basic {
    static void main() {
        int a = 5;
        int b = 2;
        int c = 5/2;   // this int / int toh it's gives you int
        System.out.println(c);

        double d = 5.0/2;  //  double/int gives you double
        System.out.println(d);

        double e = 5.0/2.0; // double/double gives you double value
        System.out.println(e);

        double f = 5/2.0;   // int/double gives you double
        System.out.println(f);
    }
}
