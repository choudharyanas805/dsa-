package basic_cheeze_JAVA;

public class variable_basic {
    static void main() {
        int x = 5;
        System.out.println(x);
        System.out.println(x+5); // yah thoda shortcut hogya intialize or declare ka

        // but we have another method to do this same thing ( OR)
        int y;  //declaration
        y = 5;  // initialization
        System.out.println(y);
        System.out.println(y+5);

    }
}
